package com.apiverve.websitetotext.data;

import com.fasterxml.jackson.annotation.*;

public class WebsitetoTextData {
    private Object date;
    private String description;
    private String title;
    private String titleAlt;
    private String text;
    private String language;
    private Object publisher;
    private String url;

    @JsonProperty("date")
    public Object getDate() { return date; }
    @JsonProperty("date")
    public void setDate(Object value) { this.date = value; }

    @JsonProperty("description")
    public String getDescription() { return description; }
    @JsonProperty("description")
    public void setDescription(String value) { this.description = value; }

    @JsonProperty("title")
    public String getTitle() { return title; }
    @JsonProperty("title")
    public void setTitle(String value) { this.title = value; }

    @JsonProperty("title_alt")
    public String getTitleAlt() { return titleAlt; }
    @JsonProperty("title_alt")
    public void setTitleAlt(String value) { this.titleAlt = value; }

    @JsonProperty("text")
    public String getText() { return text; }
    @JsonProperty("text")
    public void setText(String value) { this.text = value; }

    @JsonProperty("language")
    public String getLanguage() { return language; }
    @JsonProperty("language")
    public void setLanguage(String value) { this.language = value; }

    @JsonProperty("publisher")
    public Object getPublisher() { return publisher; }
    @JsonProperty("publisher")
    public void setPublisher(Object value) { this.publisher = value; }

    @JsonProperty("url")
    public String getURL() { return url; }
    @JsonProperty("url")
    public void setURL(String value) { this.url = value; }
}